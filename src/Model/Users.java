package Model;


public class Users {

    public int userID;
    public String userName;
    public String userPass;


    /**
     * @return the userID
     */
    public int getUserID(){
        return userID;
    }

    /**
     * @return the userName
     */
    public String getUserName() {
        return userName;
    }

    /**
     * @return the userPassword
     */
    public String getUserPass() {
        return userPass;
    }
}
